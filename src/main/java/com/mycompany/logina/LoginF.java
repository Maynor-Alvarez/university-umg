    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String usuario = jTextField1.getText();
        String password = jPasswordField1.getText();

        if("admin".equals(usuario)&&"admin".equals(password)){
            Administrador a = new Administrador();
            a.setVisible(true);
            this.dispose();
            
        }

       
        else{
            boolean encontrado = false;
            for(Profesor p : LoginA.profesores){
                if(p.password.equals(password)&&p.usuario.equals(usuario)){
                    LoginA.profesorLogeado = p;
                    BienvenidaProfesor b = new BienvenidaProfesor();
                    b.setVisible(true);
                    encontrado = true;
                    this.dispose();
                }
            }
            if(!encontrado){
     
            for(Alumno a: LoginA.alumnos){
                if(a.password.equals(password)&&a.carne.equals(usuario)){
                    LoginA.alumnoLogeado = a;
                    BienvenidaAlumno b = new BienvenidaAlumno();
                    b.setVisible(true);
                    encontrado = true;
                    this.dispose();
                }
            }
            if(!encontrado){
                JOptionPane.showMessageDialog(this, "Usuario o password incorrecto.");
            }
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed


